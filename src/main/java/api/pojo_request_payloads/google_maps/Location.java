package api.pojo_request_payloads.google_maps;

public class Location {
    private double lat;
    private double ing;

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getIng() {
        return ing;
    }

    public void setIng(double ing) {
        this.ing = ing;
    }
}
